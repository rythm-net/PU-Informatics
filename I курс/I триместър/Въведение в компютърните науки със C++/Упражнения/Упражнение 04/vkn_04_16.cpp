#include <iostream>
using namespace std;

int main() {
    char ch1, ch2;
    cout << "Два знака: ";
    cin >> ch1 >> ch2;
    cout << "ch1 = \'" << ch1 << "\'\n";
    cout << "(int)ch1 = " << (int) ch1 << "\n";
    cout << "ch2 = \'" << ch2 << "\'\n";
    cout << "(int)ch2 = " << (int) ch2 << "\n";
}